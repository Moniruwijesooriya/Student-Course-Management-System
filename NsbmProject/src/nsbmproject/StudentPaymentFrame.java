    private void txt_stu_idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_stu_idActionPerformed

        String stuID = txt_stu_id.getText();

        String[] cur_sem = db.getStudentCurrentSemester(stuID);
        String[] cur_yer = db.getStudentCurrentYear(stuID);
        String[] nam = db.getStudentName(stuID);
        String[] sub = dba.getStudentUgSubjects(stuID, cur_sem[0], cur_yer[0], fac);
        //setting the fileds of the frame

        txt_stu_name.setText(nam[0]);
        txt_year.setText(cur_yer[0]);
        txt_semester.setText(cur_sem[0]);
        //setting subject fees
        int sub1[] = dba.getStudentUgSubjectFee(sub[0], fac);
        int sub2[] = dba.getStudentUgSubjectFee(sub[1], fac);
        int sub3[] = dba.getStudentUgSubjectFee(sub[2], fac);
        int sub4[] = dba.getStudentUgSubjectFee(sub[3], fac);
        int sub5[] = dba.getStudentUgSubjectFee(sub[4], fac);
        int sub6[] = dba.getStudentUgSubjectFee(sub[5], fac);

        int total = sub1[0] + sub2[0] + sub3[0] + sub4[0] + sub5[0] + sub6[0];

        pay_subject_1.setText(Integer.toString(sub1[0]));
        pay_subject_2.setText(Integer.toString(sub2[0]));
        pay_subject_3.setText(Integer.toString(sub3[0]));
        pay_subject_4.setText(Integer.toString(sub4[0]));
        pay_subject_5.setText(Integer.toString(sub5[0]));
        pay_subject_6.setText(Integer.toString(sub6[0]));

        total_amount.setText(Integer.toString(total));
    }//GEN-LAST:event_txt_stu_idActionPerformed

