    private void StudentIDOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StudentIDOkActionPerformed

        String year, semester, stu_id;
        year = combo_year.getSelectedItem().toString();
        semester = combo_semester.getSelectedItem().toString();
        stu_id = txt_stu_id.getText();
        String stu_name[] = dbs.getStudentName(stu_id);

        if (stu_name[0] == null) {
            JOptionPane.showMessageDialog(this, "Invalid StudentID !");

        } else {
            if (mode == 1) { //mode 1 displays the assignment marks enter form
                //String subArray[] = dba.getStudentSubjects(stu_id, semester, year, fac);
                EnterAssignmentMarksFrame eamf = new EnterAssignmentMarksFrame();
                eamf.setVisible(true);
                eamf.setInfo(stu_name[0], stu_id, year, semester);
                eamf.setSubjects(stu_id, semester, year, fac);
                eamf.stuID = stu_id;
                eamf.semester = semester;
                eamf.year = year;
                eamf.faculty = fac;
                
                //eamf.setSubjects(subArray);
            } else if (mode == 2) { // mode 2 displays the exam marks enter form
                //String subArray[] = dba.getStudentSubjects(stu_id, semester, year, fac);
                EnterExamMarksFrame eemf = new EnterExamMarksFrame();
                eemf.setVisible(true);
                eemf.setInfo(stu_name[0], stu_id, year, semester);
               
                eemf.stuID = stu_id;
                eemf.semester = semester;
                eemf.year = year;
                eemf.faculty = fac;
                
                eemf.setSubject();
                
            }
            else if(mode==3){ // mode 3 displays the student result for the semester
                StudentResultFrame srf=new StudentResultFrame();
                srf.setVisible(true);
                srf.stuID = stu_id;
                srf.semester = semester;
                srf.year = year;
                srf.faculty = fac;
                
                srf.setInfo(stu_name[0], stu_id, year, semester);
                srf.setSubject();
                srf.setGrades();
                
                
            }
        }

    }//GEN-LAST:event_StudentIDOkActionPerformed
    
