    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        int y=db.login(txt_username.getText(),txt_password.getText());
        if(y==1){
            JOptionPane.showMessageDialog(this, "Login is Successful!");
            dispose();
            MainPanelFrame mpf=new MainPanelFrame();
            mpf.setVisible(true);
            
        }
        else if(y==2){
            JOptionPane.showMessageDialog(this, "Wrong Password!");
            txt_password.setText("");
        }
        else if(y==3){
            JOptionPane.showMessageDialog(this, "Invalid Username!");
            txt_username.setText("");
            txt_password.setText("");
        }
        else if(y==0){
            JOptionPane.showMessageDialog(this, "Error!");
        }
        else if(y==9){
            JOptionPane.showMessageDialog(this, "Username alreddsdsdts!");
        }
    }//GEN-LAST:event_LoginActionPerformed

