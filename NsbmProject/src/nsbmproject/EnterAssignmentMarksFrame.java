    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed

        StudentAssignmentResult sar = new StudentAssignmentResult();
        sar.setStu_id(stuID);
        sar.setSemester(semester);
        sar.setYear(year);
        sar.setFaculty(faculty);
        sar.setSubject(combo_subjects.getSelectedItem().toString());
        sar.setAsg_num(Integer.parseInt(txt_asgNo.getText()));
        sar.setAsg_marks(Integer.parseInt(txt_marks.getText()));

        int x = dbm.setAssignmentMarks(sar);

        int y[] = dbm.getSubjectAsgMark(stuID, sar.getSubject());
        int as_mark = y[0];
        as_mark += sar.getAsg_marks();

        int w[] = dbm.getSubjectFinalMark(stuID, sar.getSubject());
        int fin_mark = w[0];

        int fina_mark = fin_mark + sar.getAsg_marks();

        StudentExamResult ser = new StudentExamResult();
        ser.setStu_id(stuID);
        ser.setSubject(sar.getSubject());
        ser.setAsg_mark(as_mark);
        ser.setFinal_mark(fina_mark);

        int z = dbm.UpdateStudentUgAsgResult(ser);

        int ck = z + x;
        if (ck == 2) {
            JOptionPane.showMessageDialog(this, "Marks are updated Successfully..");
            dispose();
        }
        else{
         JOptionPane.showMessageDialog(this, "Error Occured! Try Again");
            txt_asgNo.setText("");
            txt_marks.setText("");
        }


    }//GEN-LAST:event_submitActionPerformed

