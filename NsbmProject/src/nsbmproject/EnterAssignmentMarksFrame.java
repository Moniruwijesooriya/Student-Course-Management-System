    void setSubjects(String stu_id, String semester, String year, String fac) {
        try {

            con = (Connection) DriverManager.getConnection(url, username, password);
            String query = "SELECT com_sub1,com_sub2,com_sub3,com_sub4,opt_sub1,opt_sub2 FROM student_ug_subjects WHERE stu_id=? AND faculty=? AND semester=? AND Year=?";
            pst = (PreparedStatement) con.prepareStatement(query);
            pst.setString(1, stu_id);
            pst.setString(2, fac);
            pst.setString(3, semester);
            pst.setString(4, year);
            res = pst.executeQuery();
            int j = 0;
            while (res.next()) {
                combo_subjects.addItem(res.getString(1));
                combo_subjects.addItem(res.getString(2));
                combo_subjects.addItem(res.getString(3));
                combo_subjects.addItem(res.getString(4));
                combo_subjects.addItem(res.getString(5));
                combo_subjects.addItem(res.getString(6));
                j++;
            }

        } catch (Exception e) {
            System.out.println(e);

        }
    }
